public class ClasePersona {


    private int peso;
    private int estatura;

    public ClasePersona(int peso, int estatura) {
        this.peso = peso;
        this.estatura = estatura;
    }

    public int getPeso() {
        return peso;
    }

    public void setPeso(int peso) {
        this.peso = peso;
    }

    public int getEstatura() {
        return estatura;
    }

    public void setEstatura(int estatura) {
        this.estatura = estatura;
    }


}
